#ifndef ADIAR_DOMAIN_H
#define ADIAR_DOMAIN_H

#include <adiar/file.h>

namespace adiar
{
  //////////////////////////////////////////////////////////////////////////////
  /// \brief  Set the domain for all of Adiar.
  //////////////////////////////////////////////////////////////////////////////
  void adiar_set_domain(const label_file &dom);

  //////////////////////////////////////////////////////////////////////////////
  /// \brief  Whether Adiar has a set domain yet.
  //////////////////////////////////////////////////////////////////////////////
  bool adiar_has_domain();

  //////////////////////////////////////////////////////////////////////////////
  /// \brief  Returns the set domain (or throws exception if it does not exist)
  //////////////////////////////////////////////////////////////////////////////
  label_file adiar_get_domain();
}

#endif // ADIAR_DOMAIN_H
